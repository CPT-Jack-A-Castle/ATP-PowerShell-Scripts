<#
.SYNOPSIS

Delete Windows Defender Advanced related files from PendingFileRenameOperations list.
#>
Set-StrictMode -Version Latest

function Remove-AllChildItem
{
    param([Parameter(Mandatory)][string]$Path)

    Write-Verbose "Removing all in $Path"

    foreach ($Item in Get-ChildItem -Recurse $Path -ErrorAction Ignore)
    {
        Write-Verbose "Removing $($Item.FullName)"
        Remove-Item $Item.FullName -Force -ErrorAction Ignore
    }
}

$SESSION_MANAGER = 'HKLM:\SYSTEM\CurrentControlSet\Control\Session Manager'
$PENDING_FILE_RENAME_OPERATIONS = 'PendingFileRenameOperations'
$WDATP = '*Windows Defender Advanced Threat Protection*'

$Pending = (Get-ItemProperty $SESSION_MANAGER).$PENDING_FILE_RENAME_OPERATIONS
$NewPending = @()

# Iterate all Windows Defender Advanced files without Temp
foreach ($Path in $Pending)
{
    # Leave all non WDATP related
    if ($Path -notlike $WDATP)
    {
        $NewPending += $Path
        continue
    }

    if ($Path -like "$WDATP\Temp") { continue }

    # Remove \??\ prefix
    $Path = $Path -replace '^\\\?\?\\'

    # Skip non existent
    if (!(Test-Path $Path -ErrorAction Ignore)) { continue }

    # Try removing
    Remove-Item $Path -Force -Recurse -ErrorVariable RemoveError -ErrorAction SilentlyContinue
    if ($null -ne $RemoveError)
    {
        # Do not add DataCollection
        if ($Path -like "*DataCollection*") { continue }

        $NewPending += $Path
    }
}

# Remove Temp
Remove-AllChildItem "$env:ALLUSERSPROFILE\Microsoft\Windows Defender Advanced Threat Protection\Temp"

# Update registry value
Set-ItemProperty $SESSION_MANAGER -Name $PENDING_FILE_RENAME_OPERATIONS -Value $NewPending

